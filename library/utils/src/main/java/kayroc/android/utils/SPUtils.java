package kayroc.android.utils;

import android.annotation.SuppressLint;
import android.content.Context;
import android.content.SharedPreferences;

import androidx.annotation.NonNull;

import java.util.Collections;
import java.util.HashMap;
import java.util.Map;
import java.util.Set;

import kayroc.android.ToolKitUtils;

/**
 * SharedPreference 工具类
 *
 * @author kayroc
 */
@SuppressWarnings({"unused", "FieldMayBeFinal"})
@SuppressLint("ApplySharedPref")
public final class SPUtils {

    private SPUtils() {
        throw new UnsupportedOperationException("cannot be instantiated");
    }

    private static final Map<String, SPUtils> sSpUtilsMap = new HashMap<>();
    private SharedPreferences mSp;

    /**
     * 获取SP实例
     *
     * @return {@link SPUtils}
     */
    public static SPUtils getInstance() {
        return getInstance("");
    }

    /**
     * 获取SP实例
     *
     * @param spName sp名
     *
     * @return {@link SPUtils}
     */
    public static SPUtils getInstance(String spName) {
        if (isSpace(spName)) {
            spName = "SpUtils";
        }
        SPUtils sp = sSpUtilsMap.get(spName);
        if (sp == null) {
            sp = new SPUtils(spName);
            sSpUtilsMap.put(spName, sp);
        }
        return sp;
    }

    private SPUtils(String spName) {
        mSp = ToolKitUtils.getContext().getSharedPreferences(spName, Context.MODE_PRIVATE);
    }

    /**
     * SP中写入String
     *
     * @param key   键
     * @param value 值
     */
    public void put(
        @NonNull String key,
        @NonNull String value
    ) {
        mSp.edit().putString(key, value).apply();
    }

    /**
     * SP中读取String
     *
     * @param key 键
     *
     * @return 存在返回对应值，不存在返回默认值{@code null}
     */
    public String getString(@NonNull String key) {
        return getString(key, "");
    }

    /**
     * SP中读取String
     *
     * @param key          键
     * @param defaultValue 默认值
     *
     * @return 存在返回对应值，不存在返回默认值{@code defaultValue}
     */
    public String getString(
        @NonNull String key,
        @NonNull String defaultValue
    ) {
        return mSp.getString(key, defaultValue);
    }

    /**
     * SP中写入int
     *
     * @param key   键
     * @param value 值
     */
    public void put(
        @NonNull String key,
        int value
    ) {
        mSp.edit().putInt(key, value).apply();
    }

    /**
     * SP中读取int
     *
     * @param key 键
     *
     * @return 存在返回对应值，不存在返回默认值-1
     */
    public int getInt(@NonNull String key) {
        return getInt(key, -1);
    }

    /**
     * SP中读取int
     *
     * @param key          键
     * @param defaultValue 默认值
     *
     * @return 存在返回对应值，不存在返回默认值{@code defaultValue}
     */
    public int getInt(
        @NonNull String key,
        int defaultValue
    ) {
        return mSp.getInt(key, defaultValue);
    }

    /**
     * SP中写入long
     *
     * @param key   键
     * @param value 值
     */
    public void put(
        @NonNull String key,
        long value
    ) {
        mSp.edit().putLong(key, value).apply();
    }

    /**
     * SP中读取long
     *
     * @param key 键
     *
     * @return 存在返回对应值，不存在返回默认值-1
     */
    public long getLong(@NonNull String key) {
        return getLong(key, -1L);
    }

    /**
     * SP中读取long
     *
     * @param key          键
     * @param defaultValue 默认值
     *
     * @return 存在返回对应值，不存在返回默认值{@code defaultValue}
     */
    public long getLong(
        @NonNull String key,
        long defaultValue
    ) {
        return mSp.getLong(key, defaultValue);
    }

    /**
     * SP中写入float
     *
     * @param key   键
     * @param value 值
     */
    public void put(
        @NonNull String key,
        float value
    ) {
        mSp.edit().putFloat(key, value).apply();
    }

    /**
     * SP中读取float
     *
     * @param key 键
     *
     * @return 存在返回对应值，不存在返回默认值-1
     */
    public float getFloat(@NonNull String key) {
        return getFloat(key, -1f);
    }

    /**
     * SP中读取float
     *
     * @param key          键
     * @param defaultValue 默认值
     *
     * @return 存在返回对应值，不存在返回默认值{@code defaultValue}
     */
    public float getFloat(
        @NonNull String key,
        float defaultValue
    ) {
        return mSp.getFloat(key, defaultValue);
    }

    /**
     * SP中写入boolean
     *
     * @param key   键
     * @param value 值
     */
    public void put(
        @NonNull String key,
        boolean value
    ) {
        mSp.edit().putBoolean(key, value).apply();
    }

    /**
     * SP中读取boolean
     *
     * @param key 键
     *
     * @return 存在返回对应值，不存在返回默认值{@code false}
     */
    public boolean getBoolean(@NonNull String key) {
        return getBoolean(key, false);
    }

    /**
     * SP中读取boolean
     *
     * @param key          键
     * @param defaultValue 默认值
     *
     * @return 存在返回对应值，不存在返回默认值{@code defaultValue}
     */
    public boolean getBoolean(
        @NonNull String key,
        boolean defaultValue
    ) {
        return mSp.getBoolean(key, defaultValue);
    }

    /**
     * SP中写入String集合
     *
     * @param key    键
     * @param values 值
     */
    public void put(
        @NonNull String key,
        @NonNull Set<String> values
    ) {
        mSp.edit().putStringSet(key, values).apply();
    }

    /**
     * SP中读取StringSet
     *
     * @param key 键
     *
     * @return 存在返回对应值，不存在返回默认值{@code null}
     */
    public Set<String> getStringSet(@NonNull String key) {
        return getStringSet(key, Collections.emptySet());
    }

    /**
     * SP中读取StringSet
     *
     * @param key          键
     * @param defaultValue 默认值
     *
     * @return 存在返回对应值，不存在返回默认值{@code defaultValue}
     */
    public Set<String> getStringSet(
        @NonNull String key,
        @NonNull Set<String> defaultValue
    ) {
        return mSp.getStringSet(key, defaultValue);
    }

    /**
     * SP中获取所有键值对
     *
     * @return Map对象
     */
    public Map<String, ?> getAll() {
        return mSp.getAll();
    }

    /**
     * SP中移除该key
     *
     * @param key 键
     */
    public void remove(@NonNull String key) {
        mSp.edit().remove(key).apply();
    }

    /**
     * SP中是否存在该key
     *
     * @param key 键
     *
     * @return {@code true}: 存在<br>{@code false}: 不存在
     */
    public boolean contains(@NonNull String key) {
        return mSp.contains(key);
    }

    /**
     * SP中清除所有数据
     */
    public void clear() {
        mSp.edit().clear().apply();
    }

    private static boolean isSpace(String s) {
        if (s == null) {
            return true;
        }
        for (int i = 0, len = s.length(); i < len; ++i) {
            if (!Character.isWhitespace(s.charAt(i))) {
                return false;
            }
        }
        return true;
    }
}
